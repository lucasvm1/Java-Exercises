public class Main {
    public static void main(String[] args) {

        // Declaração de Variáveis
        boolean a = false;
        boolean b = true;

        if(a && b) {
            System.out.println("Os dois são verdadeiros!");
        } else if(!a && !b) {
            System.out.println("Os dois são falsos!");
        } else {
            System.out.println("Um é verdadeiro e outro falso!");
        }

    }
}
